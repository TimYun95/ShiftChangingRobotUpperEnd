MainWindow::MainWindow(QWidget *parent) :
    QMainWindow(parent),
    ui(new Ui::MainWindow)
{
    ui->setupUi(this);

    this->setWindowTitle(tr("5轴自动驾驶机器人")); // 设置标题
    this->setWindowState(Qt::WindowMaximized); // 最大化窗体

    // 检查配置等文件夹是否存在 不存在的要新建
    QDir maindir( QString::fromStdString(Configuration::mainFolder) );
    if (!maindir.exists())
    {
        maindir.mkdir(QString::fromStdString(Configuration::mainFolder));
    }

    QDir sysdir( QString::fromStdString(Configuration::sysFilePath) );
    if (!sysdir.exists())
    {
        sysdir.mkdir(QString::fromStdString(Configuration::sysFilePath));
    }

    QDir logdir( QString::fromStdString(Configuration::logFilePath) );
    if (!logdir.exists())
    {
        logdir.mkdir(QString::fromStdString(Configuration::logFilePath));
    }
    QDir logcuvdir( QString::fromStdString(Configuration::logCurvePath) );
    if (!logcuvdir.exists())
    {
        logcuvdir.mkdir(QString::fromStdString(Configuration::logCurvePath));
    }
    QDir logcoddir( QString::fromStdString(Configuration::logCodePath) );
    if (!logcoddir.exists())
    {
        logcoddir.mkdir(QString::fromStdString(Configuration::logCodePath));
    }

    QDir sdddir( QString::fromStdString(Configuration::stdFilePath) );
    if (!sdddir.exists())
    {
        sdddir.mkdir(QString::fromStdString(Configuration::stdFilePath));
    }

    // 检查配置等文件是否存在 不存在的创建
    QFileInfo logConfInfo( QString::fromStdString(Configuration::logCodeConfPath) );
    if(!logConfInfo.isFile())
    {
        if ( !createLogProperties() )
        {
            PRINTF(LOG_WARNING, "%s: can not create the file(%s)", __func__, Configuration::logCodeConfPath.c_str());
        }
    }

    QFileInfo NEDCInfo( QString::fromStdString(Configuration::stdFilePath + "NEDC") );
    if(!NEDCInfo.isFile())
    {
        if ( !createNEDC() )
        {
            PRINTF(LOG_WARNING, "%s: can not create the file(%s)", __func__, (Configuration::stdFilePath + "NEDC").c_str());
        }
    }
    QFileInfo NEDCARMInfo( QString::fromStdString(Configuration::stdFilePath + "NEDC_ARM") );
    if(!NEDCARMInfo.isFile())
    {
        if ( !createNEDC_ARM() )
        {
            PRINTF(LOG_WARNING, "%s: can not create the file(%s)", __func__, (Configuration::stdFilePath + "NEDC_ARM").c_str());
        }
    }

    QFileInfo WLTCInfo( QString::fromStdString(Configuration::stdFilePath + "WLTC") );
    if(!WLTCInfo.isFile())
    {
        if ( !createWLTC() )
        {
            PRINTF(LOG_WARNING, "%s: can not create the file(%s)", __func__, (Configuration::stdFilePath + "WLTC").c_str());
        }
    }
    QFileInfo WLTCARMInfo( QString::fromStdString(Configuration::stdFilePath + "WLTC_ARM") );
    if(!WLTCARMInfo.isFile())
    {
        if ( !createWLTC_ARM() )
        {
            PRINTF(LOG_WARNING, "%s: can not create the file(%s)", __func__, (Configuration::stdFilePath + "WLTC_ARM").c_str());
        }
    }

    QFileInfo SCARMInfo( QString::fromStdString(Configuration::stdFilePath + "SC_ARM") );
    if(!SCARMInfo.isFile())
    {
        if ( !createSC_ARM() )
        {
            PRINTF(LOG_WARNING, "%s: can not create the file(%s)", __func__, (Configuration::stdFilePath + "SC_ARM").c_str());
        }
    }

    robotclient = AutoDriveRobotApiClient::GetInstance(); // 首次实例化客户端
    robotclient->StartClient("127.0.0.1", 2);

    QFont ft;
    ft.setPointSize(20);
    statusLabel = new QLabel();
    statusLabel->setFont(ft);
    statusLabel->setAlignment(Qt::AlignVCenter | Qt::AlignLeft);
    timeLabel = new QLabel();
    timeLabel->setFont(ft);
    timeLabel->setAlignment(Qt::AlignVCenter | Qt::AlignRight);

    pedalui = new PedalRobotUI(this, statusLabel, timeLabel); // 踏板界面添加

    vLayout =new QVBoxLayout();
    vLayout->addWidget(pedalui);
    hLayout=new QHBoxLayout();
    hLayout->addWidget(statusLabel);
    hLayout->addWidget(timeLabel);
    vLayout->addLayout(hLayout);
    ui->centralWidget->setLayout(vLayout);
}

MainWindow::~MainWindow()
{
    robotclient->StopClient();

#ifdef DEBUG_OUTPUT_2_LOCAL_FILE // 输出日志到本地文件
    Log4QTWrapper::Stop();
#endif

    delete hLayout;
    delete vLayout;
    delete statusLabel;
    delete timeLabel;
    delete pedalui;
    delete ui;
}

